import java.util.Scanner;
public class For_loop2 {
    public static void main(String[] args) {
        System.out.println("Enter number :");
        int num;
        Scanner s=new Scanner(System.in);
        num=s.nextInt();

                
        for(int i=1;i<=i;i++){
            for(int j=1;j<=num;j++){
                System.out.print(" * ");

            }
            System.out.println();
        }



        
    }
    
}
